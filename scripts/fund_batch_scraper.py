def generate_api_url(fund_code):
    """生成天天基金API URL"""
    return f"https://fund.eastmoney.com/pingzhongdata/{fund_code}.js"

def generate_fund_url(fund_code):
    """生成天天基金持仓页面URL"""
    return f"http://fundf10.eastmoney.com/ccmx_{fund_code}.html"

